const getIcon = (iconName) => {
  return iconMap[iconName] || iconMap.home;
};

const MenuItem = ({ menu, location }) => {
  const isActive = location.pathname === menu.route;
  const hasChildren = menu.children && menu.children.length > 0;

  return (
    <div>
      <Link
        to={menu.route || '#'}
        className={`flex items-center px-4 py-2 text-sm font-medium rounded-md transition-colors ${
          isActive
            ? 'bg-blue-100 text-blue-900'
            : 'text-gray-700 hover:bg-gray-100'
        }`}
      >
        <span className="mr-3">{getIcon(menu.icon)}</span>
        <span>{menu.label}</span>
      </Link>
      {hasChildren && (
        <div className="ml-8 mt-1 space-y-1">
          {menu.children.map((child) => (
            <MenuItem key={child.id} menu={child} location={location} />
          ))}
        </div>
      )}
    </div>
  );
};

const Sidebar = () => {
  const { menus, loading } = useAuth();
  const location = useLocation();

  if (loading) {
    return (
      <aside className="w-64 bg-white shadow-lg h-screen fixed left-0 top-0 overflow-y-auto">
        <div className="p-4">
          <div className="animate-pulse space-y-4">
            <div className="h-4 bg-gray-200 rounded w-3/4"></div>
            <div className="h-4 bg-gray-200 rounded w-1/2"></div>
            <div className="h-4 bg-gray-200 rounded w-2/3"></div>
          </div>
        </div>
      </aside>
    );
  }

  return (
    <aside className="w-64 bg-white shadow-lg h-screen fixed left-0 top-0 overflow-y-auto">
      <div className="p-4">
        <h2 className="text-lg font-semibold text-gray-800 mb-4">Menu</h2>
        <nav className="space-y-1">
          {menus.length === 0 ? (
            <p className="text-sm text-gray-500">No menu items available</p>
          ) : (
            menus.map((menu) => (
              <MenuItem key={menu.id} menu={menu} location={location} />
            ))
          )}
        </nav>
      </div>
    </aside>
  );
};

export default Sidebar;

